{
  //inheritance

  class Parent {
    name: string;
    age: number;
    address: string;

    constructor(name: string, age: number, address: string) {
      this.name = name;
      this.age = age;
      this.address = address;
    }

    getSleep(numberOfHours: number) {
      console.log(`${this.name} sleeps for ${numberOfHours} hours.`);
    }
  }

  class Student extends Parent {
    constructor(name: string, age: number, address: string) {
      super(name, age, address);
    }
  }

  const student = new Student("mra", 18, "ctg");
  student.getSleep(8);

  class Teacher extends Parent {
    designation: string;

    constructor(
      name: string,
      age: number,
      address: string,
      designation: string
    ) {
      super(name, age, address);
      this.designation = designation;
    }

    takeClass(numberOfClass: number) {
      console.log(`${this.name} took ${numberOfClass} classes today.`);
    }
  }

  const teacher = new Teacher("mrb", 38, "ctg", "teacher");
  teacher.takeClass(5);
}
